# Type casting adalah cara mengubah tipe data
# str(),int(),float(),bool()

# CONTOH PENGGUNAAAN

nama = "Niko" # ini adalah tipe data string
print(type(nama))
usia = 18 # ini adalah tipe data integer
print(type(usia))
ipk = 3.5 # ini adalah tipe data floating
print(type(ipk))
lulus = True # ini adalah tipe data boolean
print(type(lulus))

# MENGUBAH TIPE DATA

ipk = int(ipk)
print(type(ipk)) # Awalnya ipk bertipe floating,setelah diubah menjadi integer






