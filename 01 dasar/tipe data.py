# Ada 4 tipe data utama dalam Python,yaitu Integer(int),Floating(float),String(str),dan Boolean(bool)

# Integer(int) adalah tipe data berupa angka bulat
# Floating(float) adalah tipa data berupa desimal
# String(str) adalah tipe data teks/karakter yang diapit tanda petik tunggal(') ataupun petik ganda(") di awal dan di akhir
# Boolean(bool) adalah tipe data yang menunjukkan "Iya/benar" atau "Tidak/salah". Boolean akan sangat berguna di bagian if-else nanti

# contoh dari 4 tipe data

angka = 10 # ini adalah contoh data integer
desimal = 10.5 # ini adalah contoh data floating
sapa = "Hi semua" # ini adalah contoh data string
benar = True # ini adalah contoh data boolean

# Cara mengecek tipe data

# bisa menggunakan function type()
print(type(angka)) # ini akan menunjukkan tipe data variabel angka <class 'int'>
print(type(desimal)) # ini akan menunjukkan tipe data variabel desimal <class 'float'>
print(type(sapa)) # ini akan menunjukkan tipe data variabel sapa <class 'str'>
print(type(benar)) # ini akan menunjukkan tipe data variabel benar <class 'bool'>





