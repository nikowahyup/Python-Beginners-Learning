# Variabel adalah tempat penyimpanan data. Jika dalam matematika kita sering menggunakan variabel x untuk memisalkan sebuah nilai.
# Sama seperti variabel dalam matematika,variabel di pemrograman bisa untuk menyimpan nilai/data

# Contoh membuat variabel dan menyimpan suatu data

nama = "Niko Wahyu Pratama" # variabel "nama" menyimpan data string "Niko Wahyu Pratama"
usia = 18 # Variabel "usia" menyimpan data integer bernilai 18

# Nantinya variabel-variabel ini bisa digunakan dalam kode selanjutnya,contohnya:
#print perkenalan menggunakan format-string/f-string

print(f"nama saya {nama}. Usia saya sekarang {usia} tahun")







